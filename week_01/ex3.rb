# How does % work? Another way to say it is, "X divided by Y with J remaining."
# For example, "100 divided by 16 with 4 remaining."
# The result of % is the J part, or the remaining part.
# prints the string together with the hash calculation
puts "Roosters #{100 - 25 * 3 % 4}"

